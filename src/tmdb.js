const API_KEY = '8c0d5260584e1b495611682625c082e0';
const API_BASE = 'https://api.themoviedb.org/3';

const basicFetch = async (endpoint) => {
    const req = await fetch(`${API_BASE}${endpoint}`);
    const json = req.json();
    return json;
}

